    int val = (int)value;
    fan_intensity_level->value() = val;
    
    ventilation_ctrl->set_fan_intensity(val);
    fan_speed_update->execute();
    ui_active->value() = true;
    update_leds->execute();
    ui_timeout_script->execute();
