/// @brief Applies CO2-based auto-control if enabled and SCD41 is connected.
/// Checks sensor availability (NaN = not connected), calculates target level,
/// and applies it only if it differs from the current level (gradual change).
inline void apply_co2_auto_control() {
    // Guard: System-wide Automatik mode must be active
    if (auto_mode_active == nullptr || !auto_mode_active->value()) return;

    // Guard: CO2 auto-control (switch) must be enabled
    if (!co2_auto_enabled->value()) return;

    // Guard: System must be on
    if (!ventilation_enabled->value()) return;

    // Guard: SCD41 sensor must be connected (state is NaN when absent)
    if (scd41_co2 == nullptr) return;
    float co2_ppm = scd41_co2->state;
    if (std::isnan(co2_ppm)) {
        ESP_LOGD("co2_auto", "SCD41 not connected — CO2 auto-control inactive");
        return;
    }

    int current_level = fan_intensity_level->value();
    int min_level = automatik_min_fan_level->value();
    int max_level = automatik_max_fan_level->value();

    int target_level = VentilationLogic::get_co2_fan_level(co2_ppm, current_level, min_level, max_level);

    if (target_level != current_level) {
        ESP_LOGI("co2_auto", "CO2=%.0f ppm → Fan %d→%d (min=%d, max=%d)",
                 co2_ppm, current_level, target_level, min_level, max_level);
        fan_intensity_level->value() = target_level;
        fan_intensity_display->publish_state(target_level);
        ventilation_ctrl->set_fan_intensity(target_level);
        fan_speed_update->execute();
        update_leds->execute();
    }
}

/// @brief Core logic for the new Standard-Automatik mode.
inline void evaluate_auto_mode() {
    if (!auto_mode_active->value() || !system_on->value() || !ventilation_enabled->value()) return;

    auto *v = ventilation_ctrl;
    // Default to the configured base level from Home Assistant (Moisture protection / base ventilation)
    int target_level = automatik_min_fan_level->value(); 
    int internal_mode = v->state_machine.current_mode; // Copy current mode as starting point

    // Determine current hardware direction to correctly map NTC sensors during MODE_VENTILATION
    if (v == nullptr) return;
    const bool is_intake = v->state_machine.get_target_state(millis()).direction_in;

    // --- 1. Compute Local Temperatures ---
    float local_in = NAN;
    float local_out = NAN;

    // SCD41 is always a valid indoor temperature if available
    if (temperature != nullptr && !std::isnan(temperature->state)) {
        local_in = temperature->state;
    }

    if (internal_mode == esphome::MODE_VENTILATION) {
        // Continuous flow mode: NTC sensors show true external/internal air depending on direction
        if (is_intake) {
            // Blowing IN: Exhaust NTC gets true outside air, Supply NTC gets mixed/flow air (ignore)
            if (temp_abluft != nullptr) local_out = temp_abluft->state;
        } else {
            // Blowing OUT: Supply NTC gets true inside air, Exhaust NTC gets mixed/flow (ignore)
            if (std::isnan(local_in) && temp_zuluft != nullptr) local_in = temp_zuluft->state;
        }
    } else if (internal_mode == esphome::MODE_ECO_RECOVERY) {
        // WRG mode: Stable NTC values are valid representations (thanks to the 45s filter logic)
        if (temp_abluft != nullptr) local_out = temp_abluft->state;
        if (std::isnan(local_in) && temp_zuluft != nullptr) local_in = temp_zuluft->state;
    }
    
    // Store in controller for ESP-NOW broadcast
    v->local_t_in = local_in;
    v->local_t_out = local_out;

    // --- 2. Compute Effective Temperatures (Group-wide) ---
    float eff_in = local_in;
    float eff_out = local_out;
    
    const uint32_t now = millis();
    // Use peer indoor temp if local is missing and peer value is fresh (<5 mins)
    if (std::isnan(eff_in) && !std::isnan(v->last_peer_t_in) && (now - v->last_peer_t_in_time < PEER_TIMEOUT_MS)) {
        eff_in = v->last_peer_t_in;
    }
    // Use peer outdoor temp if local is missing and peer value is fresh (<5 mins)
    if (std::isnan(eff_out) && !std::isnan(v->last_peer_t_out) && (now - v->last_peer_t_out_time < PEER_TIMEOUT_MS)) {
        eff_out = v->last_peer_t_out;
    }

    // --- 3. Summer Cooling Logic ---
    if (!std::isnan(eff_in) && !std::isnan(eff_out)) {
        if (internal_mode != esphome::MODE_VENTILATION) {
            // Check if we should ENGAGE cooling (Inside too warm, outside is cooler)
            if (eff_in > 22.0f && eff_out < eff_in) {
                internal_mode = esphome::MODE_VENTILATION;
                ESP_LOGI("auto_mode", "Summer cooling engaged: In=%.1fC, Out=%.1fC", eff_in, eff_out);
            } else if (internal_mode != esphome::MODE_ECO_RECOVERY) {
                // Return to eco recovery
                internal_mode = esphome::MODE_ECO_RECOVERY;
            }
        } else {
            // Check if we should ABORT cooling (Outside got warmer than inside)
            if (eff_out >= eff_in) {
                internal_mode = esphome::MODE_ECO_RECOVERY;
                ESP_LOGI("auto_mode", "Summer cooling aborted (Too warm outside): In=%.1fC, Out=%.1fC", eff_in, eff_out);
            }
        }
    } else if (internal_mode != esphome::MODE_ECO_RECOVERY && internal_mode != esphome::MODE_VENTILATION) {
         // Default to WRG
         internal_mode = esphome::MODE_ECO_RECOVERY;
    }

    // --- 4. Air Quality logic (CO2 & Humidity via PID) ---
    float max_pid_demand = 0.0f;

    if (co2_auto_enabled->value() && co2_pid_result != nullptr) {
        max_pid_demand = std::max(max_pid_demand, co2_pid_result->value());
    }

    if (scd41_humidity != nullptr && outdoor_humidity != nullptr) {
        const float in_hum = scd41_humidity->state;
        const float out_hum = outdoor_humidity->state;
        
        if (!std::isnan(in_hum) && !std::isnan(out_hum)) {
            if (out_hum < in_hum && humidity_pid_result != nullptr) {
                // Only use humidity demand if outdoor is drier
                max_pid_demand = std::max(max_pid_demand, humidity_pid_result->value());
            }
        }
    }

    // Save local demand to controller for ESP-NOW broadcast
    // Every second, this device tells the group how much ventilation it currently demands.
    v->local_pid_demand = max_pid_demand;

    // Compare with peer demand if fresh (<5 mins)
    // Synchronized ESP-NOW PID Logic:
    // If a peer device in the same room reports a higher air-clearing demand (because someone
    // is breathing near it, or humidity spiked locally), we adopt that higher demand.
    // This forces all devices in the room to scale up together symmetrically, preventing
    // situations where one device runs loudly at 100% while the other idles at 10%.
    if (!std::isnan(v->last_peer_pid_demand) && (now - v->last_peer_pid_demand_time < PEER_TIMEOUT_MS)) {
        max_pid_demand = std::max(max_pid_demand, v->last_peer_pid_demand);
    }

    if (max_pid_demand > 0.01f) {
        int min_level = automatik_min_fan_level->value();
        int max_level = automatik_max_fan_level->value();
        
        // Calculate proportional LED target level (1-10 display feedback)
        // Note: The physical motor receives a precision float (0.0 - 1.0) via update_fan_logic()
        // for absolutely silent, stepless transitions. This 'target_level' is kept just so the
        // intensity LEDs on the unit faceplate show a representative average.
        float scaled_level = min_level + max_pid_demand * (max_level - min_level);
        target_level = std::max(target_level, (int)std::round(scaled_level));
        ESP_LOGD("auto_mode", "PID Demand=%.2f (Local=%.2f, Peer=%.2f) -> Boost level to %d", 
                  max_pid_demand, v->local_pid_demand, v->last_peer_pid_demand, target_level);
    }

    // 5. Presence logic
    if (radar_presence != nullptr && radar_presence->has_state() && radar_presence->state) {
        int presence_comp = auto_presence_val->value();
        if (presence_comp != 0) {
            target_level = std::max(1, std::min(10, target_level + presence_comp));
        }
    }

    // Apply the computed logic
    if (v->state_machine.current_mode != internal_mode) {
        v->set_mode((esphome::VentilationMode)internal_mode);
    }
    
    if (fan_intensity_level->value() != target_level) {
        fan_intensity_level->value() = target_level;
        fan_intensity_display->publish_state(target_level);
        ventilation_ctrl->set_fan_intensity(target_level);
        fan_speed_update->execute();
        update_leds->execute();
        ESP_LOGI("auto_mode", "Automode updated fan level to %d", target_level);
    }
}

/// @brief Sets fan PWM for the ebm-papst VarioPro reversible fan.
/// This fan uses a SINGLE PWM signal for both speed and direction:
///   - 50.0% duty cycle = STOP (active brake)
///   - 50% → 0%   = Direction: Zuluft (Stufe 1 @ 30%, Stufe 10 @ 5%)
///   - 50% → 100% = Direction: Abluft (Stufe 1 @ 70%, Stufe 10 @ 95%)
/// @param speed     Target speed as duty cycle fraction (0.1 = min, 1.0 = max).
/// @param direction Target direction: 0 = Zuluft (PWM < 50%), 1 = Abluft (PWM > 50%).
inline void set_fan_logic(float speed, int direction) {
    if (fan_pwm_primary == nullptr) return;

    // Clamp speed to valid range
    speed = std::max(0.0f, std::min(1.0f, speed));

    // Stop zone: below 5% speed or logical off
    if (speed < 0.05f) {
        fan_pwm_primary->set_level(0.50f);
        last_fan_pwm_level = 0.50f;
        return;
    }

    float pwm;
    if (direction == 0) {
        // Direction Zuluft: Min Speed (0.1) -> 30% PWM, Max Speed (1.0) -> 5% PWM
        // Linear interpolation: y = y1 + (x - x1) * (y2 - y1) / (x2 - x1)
        // PWM = 0.30 + (speed - 0.1) * (0.05 - 0.30) / (1.0 - 0.1)
        pwm = 0.30f - ((speed - 0.1f) / 0.9f) * 0.25f;
    } else {
        // Direction Abluft: Min Speed (0.1) -> 70% PWM, Max Speed (1.0) -> 95% PWM
        // PWM = 0.70 + (speed - 0.1) * (0.95 - 0.70) / (0.9)
        pwm = 0.70f + ((speed - 0.1f) / 0.9f) * 0.25f;
    }

    // Clamp to absolute hardware safety limits
    pwm = std::max(0.02f, std::min(0.98f, pwm));

    fan_pwm_primary->set_level(pwm);
    last_fan_pwm_level = pwm;
}

/// @brief Converts a user-facing level (1-10) to actual hardware PWM speed (10% - 100%)
inline float level_to_speed(float level) {
    static const float SPEED_MIN = 0.10f;  // Stufe 1 mapped to 10% PWM
    static const float MAX_FAN_LEVEL = 10.0f;
    return SPEED_MIN + ((level - 1.0f) / (MAX_FAN_LEVEL - 1.0f)) * (1.0f - SPEED_MIN);
}

/// @brief Calculates the exact target speed (0.0 to 1.0) based on intensity level and auto PID rules.
inline float get_current_target_speed() {
    if (fan_intensity_level == nullptr) return 0.0f;
    
    // Normal manual mode calculation
    float speed = level_to_speed((float)fan_intensity_level->value());

    if (auto_mode_active != nullptr && auto_mode_active->value()) {
        float max_pid_demand = 0.0f;
        if (co2_auto_enabled->value() && co2_pid_result != nullptr) {
            max_pid_demand = std::max(max_pid_demand, co2_pid_result->value());
        }
        if (humidity_pid_result != nullptr) {
             max_pid_demand = std::max(max_pid_demand, humidity_pid_result->value());
        }
        if (ventilation_ctrl != nullptr) {
            const uint32_t now = millis();
            if (!std::isnan(ventilation_ctrl->last_peer_pid_demand) && (now - ventilation_ctrl->last_peer_pid_demand_time < PEER_TIMEOUT_MS)) {
                max_pid_demand = std::max(max_pid_demand, ventilation_ctrl->last_peer_pid_demand);
            }
        }
        
        float min_level = (float)automatik_min_fan_level->value();
        float max_level = (float)automatik_max_fan_level->value();
        
        if (max_pid_demand > 0.01f) {
            // Scale the PID demand (0.0 - 1.0) directly onto the Level domain (e.g. Stage 1 to Stage 8)
            float target_level = min_level + max_pid_demand * (max_level - min_level);
            speed = level_to_speed(target_level);
        } else {
            // When demand drops to 0, use the minimum defined limit
            speed = level_to_speed(min_level);
        }
    }
    return std::max(0.0f, std::min(1.0f, speed));
}

/// @brief Updates fan speed and direction based on intensity and mode.
/// Reads fan_direction switch for direction, calculates speed from intensity/PID,
/// then calls set_fan_logic() which maps both into the single VarioPro PWM signal.
inline void update_fan_logic() {
    if (fan_intensity_level == nullptr) return;
    const int intensity = fan_intensity_level->value();

    // Dynamic Cycle duration mapped to fan level:
    if (ventilation_ctrl != nullptr) {
        float dynamic_cycle_s = 70.0f - ((intensity - 1.0f) * (20.0f / 9.0f));
        uint32_t dynamic_cycle_ms = std::round(dynamic_cycle_s) * 1000;
        if (ventilation_ctrl->state_machine.cycle_duration_ms != dynamic_cycle_ms) {
            ventilation_ctrl->set_cycle_duration(dynamic_cycle_ms);
        }
    }

    float speed = get_current_target_speed();

    // Apply software ramping factor if a controller is available
    if (ventilation_ctrl != nullptr) {
        HardwareState state = ventilation_ctrl->state_machine.get_target_state(millis());
        speed *= state.ramp_factor;
        
        // Log ramping during transition phases for debugging
        if (state.ramp_factor < 0.99f && state.ramp_factor > 0.01f) {
            ESP_LOGD("fan_ramp", "Ramping speed: %.2f (Base: %.2f, Factor: %.2f)", 
                      speed, get_current_target_speed(), state.ramp_factor);
        }
    }

    // Read current direction from the fan_direction switch:
    // OFF = Direction: Zuluft (PWM < 50%)
    // ON  = Direction: Abluft (PWM > 50%)
    const int direction = (fan_direction != nullptr && fan_direction->state) ? 1 : 0;

    set_fan_logic(speed, direction);
}

